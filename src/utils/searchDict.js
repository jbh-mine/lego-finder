// Main translation function
function translateSearchQuery(query) {
  if (!query) return query;

  var lower = query.toLowerCase().trim();
  var found = null;

  // Check phrases first
  for (var i = 0; i < PHRASE_MAP.length; i++) {
    var pair = PHRASE_MAP[i];
    if (lower === pair[0]) {
      found = pair[1];
      break;
    }
  }

  // If not found, check single words
  if (!found) {
    found = SINGLE_WORD_MAP[lower];
  }

  return found || query;
}

